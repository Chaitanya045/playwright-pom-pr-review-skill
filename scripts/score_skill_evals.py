#!/usr/bin/env python3
from __future__ import annotations

import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
CASES = ROOT / "evals" / "cases.json"
RUNS = ROOT / "evals" / "runs"
WITH_SKILL = RUNS / "with_skill.json"
WITHOUT_SKILL = RUNS / "without_skill.json"
TRIGGER = RUNS / "trigger.json"
RESULTS = ROOT / "docs" / "eval-results.md"


def load_json(path: Path):
    if not path.exists():
        print(f"missing {path.relative_to(ROOT)}", file=sys.stderr)
        return None
    return json.loads(path.read_text(encoding="utf-8"))


def normalized(value) -> str:
    return json.dumps(value, sort_keys=True).lower()


def covers(output, checks: dict[str, list[str]]) -> dict[str, bool]:
    text = normalized(output)
    return {name: all(token.lower() in text for token in tokens) for name, tokens in checks.items()}


def pct(n: int, d: int) -> str:
    if d == 0:
        return "n/a"
    return f"{(100 * n / d):.1f}%"


def main() -> int:
    cases = load_json(CASES)
    trigger_runs = load_json(TRIGGER)
    with_runs = load_json(WITH_SKILL)
    without_runs = load_json(WITHOUT_SKILL)
    if cases is None or trigger_runs is None or with_runs is None or without_runs is None:
        return 1

    by_id = {case["id"]: case for case in cases}
    positive_cases = [case for case in cases if case.get("should_trigger")]
    outcome_cases = [case for case in positive_cases if case.get("expected_checks")]

    trigger_total = 0
    trigger_pass = 0
    trigger_rows: list[list[str]] = []
    for run in trigger_runs["runs"]:
        case = by_id[run["case_id"]]
        expected = bool(case["should_trigger"])
        actual = bool(run["use_skill"])
        ok = expected == actual
        trigger_total += 1
        trigger_pass += int(ok)
        trigger_rows.append([run["case_id"], str(run["trial"]), str(expected), str(actual), "PASS" if ok else "FAIL"])

    with_total = 0
    with_covered = 0
    without_total = 0
    without_covered = 0
    outcome_rows: list[list[str]] = []
    with_by_case = {run["case_id"]: run for run in with_runs["runs"]}
    without_by_case = {run["case_id"]: run for run in without_runs["runs"]}

    for case in outcome_cases:
        checks = case["expected_checks"]
        with_cov = covers(with_by_case[case["id"]]["output"], checks)
        without_cov = covers(without_by_case[case["id"]]["output"], checks)
        with_count = sum(with_cov.values())
        without_count = sum(without_cov.values())
        total = len(checks)
        with_total += total
        with_covered += with_count
        without_total += total
        without_covered += without_count
        outcome_rows.append([
            case["id"],
            str(total),
            str(with_count),
            str(without_count),
            ", ".join(name for name, ok in with_cov.items() if not ok) or "-",
        ])

    lines = [
        "# Skill eval results",
        "",
        "Generated by `python scripts/score_skill_evals.py` after running the model outputs from `evals/cases.json`.",
        "",
        "## Summary",
        "",
        "| Metric | Value |",
        "| --- | ---: |",
        f"| Eval cases | {len(cases)} |",
        f"| Positive trigger cases | {len(positive_cases)} |",
        f"| Negative trigger cases | {len(cases) - len(positive_cases)} |",
        f"| Trigger runs | {trigger_total} |",
        f"| Trigger pass rate | {trigger_pass}/{trigger_total} ({pct(trigger_pass, trigger_total)}) |",
        f"| Outcome cases | {len(outcome_cases)} |",
        f"| With-skill expected checks covered | {with_covered}/{with_total} ({pct(with_covered, with_total)}) |",
        f"| Without-skill expected checks covered | {without_covered}/{without_total} ({pct(without_covered, without_total)}) |",
        f"| Skill delta | {with_covered - without_covered:+d} checks |",
        "",
        "## Outcome coverage by case",
        "",
        "| Case | Expected checks | With skill covered | Without skill covered | Missing with skill |",
        "| --- | ---: | ---: | ---: | --- |",
    ]
    for row in outcome_rows:
        lines.append(f"| {row[0]} | {row[1]} | {row[2]} | {row[3]} | {row[4]} |")

    lines.extend([
        "",
        "## Trigger routing runs",
        "",
        "| Case | Trial | Expected use_skill | Actual use_skill | Result |",
        "| --- | ---: | --- | --- | --- |",
    ])
    for row in trigger_rows:
        lines.append(f"| {row[0]} | {row[1]} | {row[2]} | {row[3]} | {row[4]} |")
    lines.append("")

    RESULTS.parent.mkdir(parents=True, exist_ok=True)
    RESULTS.write_text("\n".join(lines), encoding="utf-8")

    print(f"trigger={trigger_pass}/{trigger_total} with_skill={with_covered}/{with_total} without_skill={without_covered}/{without_total} delta={with_covered - without_covered:+d}")
    print(f"results={RESULTS.relative_to(ROOT)}")

    return 0 if trigger_pass == trigger_total and with_covered == with_total else 1


if __name__ == "__main__":
    raise SystemExit(main())
